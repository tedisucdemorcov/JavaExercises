public class WhileLoop {
    public static void main (String args[])
    {
        int i = 0;
        while ( i< 5)
        {
            System.out.println(i);
            i++;
        }

        int z = 14;
        while (z < 10)
        {
            if (z>5)
            {
                System.out.println("Well done!");
            }
            else {
                System.out.println("Good");
            }
        }
    }
}
